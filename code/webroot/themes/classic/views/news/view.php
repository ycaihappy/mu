                   <h1>我国再次成功进行陆基中段反导拦截技术试验</h1>
                  <div class="tit-bar clearfix">
					<div class="ll">
						<span class="color-a-0"><a target="_blank" accesskey="5" href="http://news.qq.com/newsgn/zhxw/shizhengxinwen.htm" title="更多时政新闻相关内容列表">时政新闻</a></span><span class="bor-tit"></span><span class="color-a-1" ><a target="_blank" href="http://www.xinhuanet.com">新华网</a></span><span class="infoMblog"><span class="color-a-2" bosszone="jgmblog"></span></span><span style="margin:0 8px;" class="color-a-3">钱彤</span><span class="article-time">2013-01-27 22:55</span><span class="bor-tit"></span></div>

